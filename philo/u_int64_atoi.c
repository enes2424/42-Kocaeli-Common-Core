#include <stdint.h>

u_int64_t	ft_uatoi(const char *str)
{
	u_int64_t	num;

	num = 0;
	if (*str == '+')
		str++;
	while (*str && *str >= '0' && *str <= '9')
		num = (num * 10) + (*str++ - '0');
	return (num);
}
